package haust.viakiba.weather.suggestion.entity;

public class Suggestion {

    private CarWashing carWashing;
    private Dressing dressing;
    private Flu flu;
    private Sport sport;
    private Travel travel;
    private Uv uv;
    public void setCarWashing(CarWashing carWashing) {
         this.carWashing = carWashing;
     }
     public CarWashing getCarWashing() {
         return carWashing;
     }

    public void setDressing(Dressing dressing) {
         this.dressing = dressing;
     }
     public Dressing getDressing() {
         return dressing;
     }

    public void setFlu(Flu flu) {
         this.flu = flu;
     }
     public Flu getFlu() {
         return flu;
     }

    public void setSport(Sport sport) {
         this.sport = sport;
     }
     public Sport getSport() {
         return sport;
     }

    public void setTravel(Travel travel) {
         this.travel = travel;
     }
     public Travel getTravel() {
         return travel;
     }

    public void setUv(Uv uv) {
         this.uv = uv;
     }
     public Uv getUv() {
         return uv;
     }

}
